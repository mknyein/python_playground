# String assignment
name = "John Doe"

# String concatenation
greeting = "Hello, " + name

# String length
length = len(name)

# Accessing characters by index
first_char = name[0]
last_char = name[-1]

# String slicing
substring = name[5:8]

print(greeting)    # Output: Hello, John Doe
print(length)      # Output: 8
print(first_char)  # Output: J
print(last_char)   # Output: e
print(substring)   # Output: Doe
