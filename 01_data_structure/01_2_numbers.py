# Integer
x = 10

# Float
y = 5.7

# Arithmetic operations
sum = x + y
difference = x - y
product = x * y
quotient = x / y
remainder = x % 3

print(sum)         # Output: 15.7
print(difference)  # Output: 4.3
print(product)     # Output: 57.0
print(quotient)    # Output: 1.7543859649122806
print(remainder)   # Output: 1
